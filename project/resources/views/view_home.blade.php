    <h4>Perpustakaan PNRI</h4>
    Perpustakaan Nasional Republik Indonesia (PNRI) adalah lembaga perpustakaan yang berada di bawah naungan 
    Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi Republik Indonesia. PNRI memiliki peran yang 
    sangat penting dalam mempromosikan literasi, pengetahuan, dan budaya di Indonesia melalui layanan 
    perpustakaan yang berkualitas dan berkelanjutan. Sebagai perpustakaan nasional, PNRI menjadi sumber 
    informasi yang penting bagi berbagai kalangan, mulai dari mahasiswa, peneliti, hingga masyarakat umum.

    <p>
    Sebagai institusi yang bertanggung jawab atas pengelolaan dan pengembangan perpustakaan nasional 
